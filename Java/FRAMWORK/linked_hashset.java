import java.util.*;

public class linked_hashset {
    public static void main(String args[]) {
        // Creating HashSet and adding elements
        LinkedHashSet<String> set = new LinkedHashSet<String>();
        set.add("adi");
        set.add("subham");
        set.add("adi_subham");
        System.out.println("hash set name= "+set);
    }
}